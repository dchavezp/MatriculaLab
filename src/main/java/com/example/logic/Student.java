package com.example.logic;

public class Student {
	private Integer idAlumno;
	private String nombreAlumno;
	private String estadoAlumno;
	private String cuiAlumno;
	private String apellidoAlumnno;
	private String correoAlumno;
	private String contrasenaAlumno;
	public Student() {
		
	}
	public Integer getIdAlumno() {
		return idAlumno;
	}
	public void setIdAlumno(Integer idAlumno) {
		this.idAlumno = idAlumno;
	}
	public String getNombreAlumno() {
		return nombreAlumno;
	}
	public void setNombreAlumno(String nombreAlumno) {
		this.nombreAlumno = nombreAlumno;
	}
	public String getEstadoAlumno() {
		return estadoAlumno;
	}
	public void setEstadoAlumno(String estadoAlumno) {
		this.estadoAlumno = estadoAlumno;
	}
	public String getCuiAlumno() {
		return cuiAlumno;
	}
	public void setCuiAlumno(String cuiAlumno) {
		this.cuiAlumno = cuiAlumno;
	}
	public String getApellidoAlumnno() {
		return apellidoAlumnno;
	}
	public void setApellidoAlumnno(String apellidoAlumnno) {
		this.apellidoAlumnno = apellidoAlumnno;
	}
	public String getCorreoAlumno() {
		return correoAlumno;
	}
	public void setCorreoAlumno(String correoAlumno) {
		this.correoAlumno = correoAlumno;
	}
	public String getContrasenaAlumno() {
		return contrasenaAlumno;
	}
	public void setContrasenaAlumno(String contrasenaAlumno) {
		this.contrasenaAlumno = contrasenaAlumno;
	}
	
}
